<?php

// namespace calculator;

class calculator{


public function add($x , $y){
    return $x + $y ;
}


public function sub($x , $y){
    return $x - $y ;
}


public function mul($x , $y){
    return $x * $y ;
}


public function div($x , $y){
    return $x / $y ;
}

public function mod($x , $y){
    return $x % $y;
}


}

?>